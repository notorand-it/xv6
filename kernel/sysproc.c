uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  if(n < 0)
    n = 0;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

uint64
sys_procinfo(void)
{
  struct pinfo p;
  uint64 uptr;

  argaddr(0, &uptr); 

  struct proc *curproc = myproc();

  // Fill the pinfo struct
  p.ppid = curproc->parent ? curproc->parent->pid : 0;
  p.syscall_count = curproc->syscall_count;
  p.page_usage = (curproc->sz + PGSIZE - 1) / PGSIZE; // rounding up

  // Copy the struct back to user space
  if (copyout(curproc->pagetable, uptr, (char *)&p, sizeof(p)) < 0)
    return -1;

  return 0;
}